  Future<void> _ingest(String statName, int amount, String op) async {
    final eos = _ensureInit();
    _requireSignedIn();

    final ingest = calloc<EOS_Stats_IngestData>();
    ingest.ref
      ..ApiVersion = EOS_STATS_INGESTDATA_API_LATEST
      ..StatName = statName.toNativeUtf8().cast<Char>()
      ..IngestAmount = amount;

    final opts = calloc<EOS_Stats_IngestStatOptions>();
    opts.ref
      ..ApiVersion = EOS_STATS_INGESTSTAT_API_LATEST
      ..LocalUserId = _localUserId!
      ..Stats = ingest
      ..StatsCount = 1
      ..TargetUserId = _localUserId!;

    try {
      final res = await _track(
            (_, cd) => eos.EOS_Stats_IngestStat(
          _stats!,
          opts,
          cd,
          Pointer.fromFunction<
              Void Function(Pointer<EOS_Stats_IngestStatCompleteCallbackInfo>)>(
            _onIngestStatComplete,
          ),
        ),
      );
      checkEosResult(res.code, 'EOS_Stats_IngestStat ($op)');
    } finally {
      calloc.free(ingest.ref.StatName);
      calloc.free(ingest);
      calloc.free(opts);
    }
  }

