  DateTime _now() => DateTime.now();

  Future<void> dispose() async {
    _pump?.cancel();
    _pump = null;
    final eos = _eos;
    if (eos != null && _platform != null) {
      eos.EOS_Platform_Release(_platform!);
      eos.EOS_Shutdown();
    }
    _platform = null;
    _eos = null;
    _puid = null;
    await _events.close();
  }
}