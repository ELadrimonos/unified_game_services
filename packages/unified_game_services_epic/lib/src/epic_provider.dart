  @override
  Future<void> unlockAchievement(String achievementId) async {
    final eos = _ensureInit();
    _requireSignedIn();
    final id = resolveAchievementId(achievementId);

    final ids = calloc<Pointer<Char>>(1);
    ids[0] = id.toNativeUtf8().cast<Char>();

    final opts = calloc<EOS_Achievements_UnlockAchievementsOptions>();
    opts.ref
      ..ApiVersion = EOS_ACHIEVEMENTS_UNLOCKACHIEVEMENTS_API_LATEST
      ..UserId = _localUserId!
      ..AchievementIds = ids
      ..AchievementsCount = 1;

    try {
      final res = await _track(
            (_, cd) => eos.EOS_Achievements_UnlockAchievements(
          _achievements!,
          opts,
          cd,
          Pointer.fromFunction<
              Void Function(
                  Pointer<EOS_Achievements_OnUnlockAchievementsCompleteCallbackInfo>,
                  )>(_onUnlockAchievementsComplete),
        ),
      );
      checkEosResult(res.code, 'EOS_Achievements_UnlockAchievements');
    } finally {
      calloc.free(ids[0]);
      calloc.free(ids);
      calloc.free(opts);
    }
  }

