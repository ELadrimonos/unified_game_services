  DynamicLibrary openEosLibrary({String? overridePath}) {
    if (overridePath != null) return DynamicLibrary.open(overridePath);
    final name = Platform.isWindows
        ? 'EOSSDK-Win64-Shipping.dll'
        : Platform.isMacOS
        ? 'libEOSSDK-Mac-Shipping.dylib'
        : 'libEOSSDK-Linux-Shipping.so';
    return DynamicLibrary.open(name);
  }

  static void _onEosLog(Pointer<EOS_LogMessage> msg) {
    final text = msg.ref.Message.cast<Utf8>().toDartString();
    print('🎮 [EOS NATIVE]: $text');
  }

  EosBindings _ensureInit() {
    final existing = _eos;
    if (existing != null) return existing;

    final EosBindings eos;
    try {
      eos = EosBindings(openEosLibrary(overridePath: libraryPath));
    } catch (e) {
      throw SignInFailedException('Failed to load the EOS SDK library.', e);
    }

    final initOpts = calloc<EOS_InitializeOptions>();
    initOpts.ref
      ..ApiVersion = EOS_INITIALIZE_API_LATEST
      ..ProductName = credentials.productName.toNativeUtf8().cast<Char>()
      ..ProductVersion = credentials.productVersion.toNativeUtf8().cast<Char>();
    try {
      final rc = eos.EOS_Initialize(initOpts);
      eos.EOS_Logging_SetLogLevel(
          EOS_ELogCategory.EOS_LC_ALL_CATEGORIES, EOS_ELogLevel.EOS_LOG_Info);
      eos.EOS_Logging_SetCallback(
          Pointer.fromFunction<Void Function(Pointer<EOS_LogMessage>)>(_onEosLog));
      if (rc.value != EosResult.success && rc.value != 15) {
        checkEosResult(rc.value, 'EOS_Initialize');
      }
    } finally {
      calloc.free(initOpts.ref.ProductName);
      calloc.free(initOpts.ref.ProductVersion);
      calloc.free(initOpts);
    }

    final platOpts = calloc<EOS_Platform_Options>();
    platOpts.ref
      ..ApiVersion = EOS_PLATFORM_OPTIONS_API_LATEST
      ..ProductId = credentials.productId.toNativeUtf8().cast<Char>()
      ..SandboxId = credentials.sandboxId.toNativeUtf8().cast<Char>()
      ..DeploymentId = credentials.deploymentId.toNativeUtf8().cast<Char>()
      ..bIsServer = 0; // 0 represents EOS_FALSE

    platOpts.ref.ClientCredentials
      ..ClientId = credentials.clientId.toNativeUtf8().cast<Char>()
      ..ClientSecret = credentials.clientSecret.toNativeUtf8().cast<Char>();

    final key = credentials.encryptionKey;
    if (key != null) {
      platOpts.ref.EncryptionKey = key.toNativeUtf8().cast<Char>();
    }

    try {
      _platform = eos.EOS_Platform_Create(platOpts);
    } finally {
      calloc.free(platOpts.ref.ProductId);
      calloc.free(platOpts.ref.SandboxId);
      calloc.free(platOpts.ref.DeploymentId);
      calloc.free(platOpts.ref.ClientCredentials.ClientId);
      calloc.free(platOpts.ref.ClientCredentials.ClientSecret);
      if (key != null) calloc.free(platOpts.ref.EncryptionKey);
      calloc.free(platOpts);
    }

    if (_platform == null || _platform == nullptr) {
      throw const SignInFailedException(
        'EOS_Platform_Create returned null (check credentials).',
      );
    }

    _connect = eos.EOS_Platform_GetConnectInterface(_platform!);
    print('🔌 [EOS] Connect interface: $_connect');
    _achievements = eos.EOS_Platform_GetAchievementsInterface(_platform!);
    _stats = eos.EOS_Platform_GetStatsInterface(_platform!);

    _eos = eos;
    return eos;
  }

  /// Espera a que el completador se complete, llamando a `EOS_Platform_Tick`
  /// periódicamente para permitir que los callbacks se disparen.
  Future<_CbResult> _track(
      void Function(int token, Pointer<Void> clientData) call,
      {bool treatTimeoutAsSuccess = false}) async {
    final token = _nextToken++;
    final completer = Completer<_CbResult>();
    _pending[token] = completer;

    // Asignar memoria para el token
    final tokenPtr = calloc<Int32>()..value = token;
    final clientData = tokenPtr.cast<Void>();

    // Llamar a la operación
    call(token, clientData);

    // Bucle de ticks hasta que se complete o timeout
    final stopwatch = Stopwatch()..start();
    const timeout = Duration(seconds: 30);
    while (!completer.isCompleted && stopwatch.elapsed < timeout) {
      if (_eos != null && _platform != null) {
        _eos!.EOS_Platform_Tick(_platform!);
      }
      await Future.delayed(const Duration(milliseconds: 50));
    }

    if (!completer.isCompleted) {
      _pending.remove(token);
      calloc.free(tokenPtr);
      if (treatTimeoutAsSuccess) {
        // Para CreateDeviceId, asumimos que el timeout significa que el dispositivo ya existe (código 24)
        print('⚠️ [EOS] Timeout en CreateDeviceId, asumiendo EOS_DuplicateNotAllowed (24)');
        return _CbResult(24); // EOS_DuplicateNotAllowed
      }
      throw const NetworkException('EOS operation timed out.');
    }

    // El callback ya liberó la memoria, así que no liberamos aquí.
    return await completer.future;
  }

  String? _productUserIdToString(EosBindings eos, EOS_ProductUserId? puid) {
    if (puid == null || puid == nullptr) return null;
    final buf = calloc<Char>(33);
    final len = calloc<Int32>()..value = 33;
    try {
      final rc = eos.EOS_ProductUserId_ToString(puid, buf, len);
      if (rc.value != 0) return null;
      return buf.cast<Utf8>().toDartString();
    } finally {
      calloc.free(buf);
      calloc.free(len);
    }
  }

